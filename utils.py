# Default timeout para `deep_research` (s). Pode ser sobrescrito em
# runtime por config.DEFAULT_RESEARCH_TIMEOUT_S (ver server.py).
DEFAULT_RESEARCH_TIMEOUT_S: int = 90


class RateLimiter:
    """Limitador de concorrência (semáforo) para deep_research.

    Nota de nomenclatura: isto limita quantas pesquisas rodam
    *simultaneamente* (default 3), não a *taxa* ao longo do tempo — um
    agente chamando deep_research em sequência (uma de cada vez) não é
    barrado por isto. Se o objetivo de `MED-04` for realmente limitar
    taxa (ex.: no máximo N pesquisas por minuto, mesmo sequenciais), um
    semáforo de concorrência não resolve sozinho; considerar um token
    bucket de verdade (com refill por tempo) como extensão futura se
    esse cenário se mostrar necessário na prática.

    Audit rodada 2 BUG-7: `acquire` aceita `timeout` opcional para evitar
    que uma chamada fique bloqueada para sempre se o semáforo saturar.
    """

    DEFAULT_ACQUIRE_TIMEOUT_S = 30  # janela máxima para aguardar slot

    def __init__(self, max_concurrent: int = 3, acquire_timeout_s: int | None = None):
        if max_concurrent < 1:
            raise ValueError(f"max_concurrent must be >= 1, got {max_concurrent}")
        self._max = max_concurrent
        self._acquire_timeout_s = acquire_timeout_s or self.DEFAULT_ACQUIRE_TIMEOUT_S
        # Semaphore lazy (audit rodada 2): criado no primeiro acquire, bindado
        # ao event-loop atual. Isso resolve o erro "bound to a different event
        # loop" que ocorre em testes (cada teste cria seu próprio loop).
        self._sem: asyncio.Semaphore | None = None

    def _get_sem(self) -> asyncio.Semaphore:
        """Retorna semáforo, criando novo se necessário.

        Como `asyncio.Semaphore` é vinculado ao loop onde foi criado,
        em ambientes com múltiplos loops (ex.: testes que recriam o loop
        a cada `asyncio.run`) o semáforo antigo causa
        "bound to a different event loop" no acquire.

        Estratégia (audit rodada 2): a cada acquire(), tenta usar o
        semáforo existente; em caso de loop-mismatch, recria e tenta
        de novo. Ver `_acquire_with_recreate()`.
        """
        sem = self._sem
        if sem is None:
            sem = asyncio.Semaphore(self._max)
            self._sem = sem
        return sem

    def _recreate_sem(self) -> asyncio.Semaphore:
        """Recria o semáforo (descarta slots do loop anterior)."""
        self._sem = asyncio.Semaphore(self._max)
        return self._sem

    async def _acquire_with_recreate(self) -> None:
        """Acquire que se recupera de loop-mismatch automaticamente."""
        sem = self._get_sem()
        try:
            await sem.acquire()
        except RuntimeError as exc:
            if "different event loop" in str(exc):
                sem = self._recreate_sem()
                await sem.acquire()
            else:
                raise

    async def acquire(self) -> None:
        if self._acquire_timeout_s is None:
            await self._acquire_with_recreate()
        else:
            try:
                await asyncio.wait_for(
                    self._acquire_with_recreate(),
                    timeout=self._acquire_timeout_s,
                )
            except asyncio.TimeoutError:
                raise

    def release(self) -> None:
        sem = self._sem
        if sem is not None:
            try:
                sem.release()
            except ValueError:
                # Audit rodada 3 BUG-RateLimiter: `ValueError` indica
                # "semaphore released too many times" — bug estrutural
                # (release chamado sem acquire correspondente). Logamos
                # warning para diagnosticar.
                import sys as _sys
                print(
                    "WARN: RateLimiter.release() called more times than "
                    "acquired — semaphore would overflow",
                    file=_sys.stderr,
                )
            except RuntimeError:
                # Semaphore recriado por loop-mismatch — release do
                # semáforo antigo pode levantar (loop incompat).
                # Silencia — é um caso conhecido do design lazy.
                pass

    @property
    def max_concurrent(self) -> int:
        return self._max


