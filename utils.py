# Input length limits
MAX_QUERY_LEN = 500
MAX_PROMPT_LEN = 2_000
MAX_TOPIC_LEN = 300
MAX_REPORT_FORMAT_LEN = 50

# Known prompt injection patterns.
# Detects the most common jailbreak and context-override techniques.
_INJECTION_PATTERNS = re.compile(
    r"("
    # Romanizado: en + pt
    r"ignore\s+(all\s+)?(previous|prior|above|preceding)\s+instructions?"
    r"|forget\s+(everything|all|previous|prior)"
    r"|new\s+(system\s+)?instruction"
    r"|disregard\s+(all\s+)?(previous|prior|above)\s+instructions?"
    r"|you\s+are\s+now\s+(an?\s+)?(unrestricted|jailbroken|free)"
    r"|system\s*:\s*you\s+are"
    r"|<\s*system\s*>"
    r"|<\s*/\s*system\s*>"
    r"|\[INST\]"
    r"|###\s*instruction"
    r"|act\s+as\s+(an?\s+)?(unrestricted|jailbroken|evil|malicious)"
    # Verbos alternativos para "reveal" (audit rodada 2 — BUG-3).
    # Agora cobre: reveal/tell/show/print/display/output/leak/expose/dump/share/echo/...
    r"|(?:reveal|tell|show|print|display|output|leak|expose|dump|extract|share|echo|repeat)\b[^.\n]{0,40}\b(?:system\s*prompt|api[\s_]?key|secret|password|instructions?)"
    r"|exfiltrate|exfiltra[çc][aã]o"
    r")",
    re.IGNORECASE,
)

# Char de controle = NUL (\x00), backspace (\x08), tab vertical (\x0b), form-feed
# (\x0c), carriage return (\x0d), outros ASCII 14-31 exceto newline (\x0a),
# DEL (\x7f). Incluímos \r para evitar "carriage-return injection" que apaga a
# linha original num terminal — audit rodada 2 BUG-1.
_CONTROL_CHARS_RE = re.compile(r"[\x00-\x08\x0b\x0c\x0d\x0e-\x1f\x7f]")

# Zero-width chars / Unicode direction marks usados em bypass de sanitização
# (audit rodada 2 — BUG-2).
_ZERO_WIDTH_RE = re.compile(r"[\u200b\u200c\u200d\u2060\ufeff\u2028\u2029]")


def _normalize_input(text: str) -> str:
    """Normaliza input para detecção robusta de injection.

    Aplica:
      1. NFKC Unicode normalization (homoglyphs → ASCII canônico).
      2. Remove zero-width chars (ZWS, ZWNJ, RTL marks).
      3. Remove control chars (NUL, CR, TAB vertical, etc.).
      4. Normaliza whitespace (multi-espaço → 1).
      5. Strip final.

    Retorna string normalizada. Caller deve checar contra _INJECTION_PATTERNS
    sobre o texto *normalizado* (não o original), para bloquear variações
    ofuscadas.
    """
    import unicodedata as _ud

    text = _ud.normalize("NFKC", text)
    text = _ZERO_WIDTH_RE.sub("", text)
    text = _CONTROL_CHARS_RE.sub("", text)
    text = re.sub(r"\s+", " ", text).strip()
    return text


