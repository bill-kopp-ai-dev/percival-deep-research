def format_sources_for_response(sources: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    """
    Formats research sources for API responses.

    Args:
        sources: List of source dictionaries.

    Returns:
        Formatted source list for API responses.
    """
    return [
        {
            "title": source.get("title", "Unknown"),
            "url": source.get("url", ""),
            "content_length": len(source.get("content", "")),
        }
        for source in sources
    ]


def format_sources_lines(formatted: list[dict]) -> list[str]:
    """
    Serializa a lista formatada de fontes em linhas de texto.

    Cada linha tem o formato:
        `[N] <title> | <url> | <content_length> chars`

    Hardened: title/url/content_length `None` viram defaults — nunca `'None'`
    ou `'None chars'` nas linhas geradas.
    """
    def _safe(v, default):
        return v if v is not None else default

    return [
        f"[{i}] {_safe(src.get('title'), 'Unknown')} | "
        f"{_safe(src.get('url'), '')} | "
        f"{_safe(src.get('content_length'), 0)} chars"
        for i, src in enumerate(formatted, 1)
    ]


def format_context_with_sources(
    topic: str, context: str, sources: List[Dict[str, Any]]
) -> str:
    """
    Formats the research context together with its sources for display.

    Args:
        topic: Research topic (já sanitizado pelo caller via sanitize_topic).
        context: Research context body.
        sources: List of source dictionaries (None-safe — tratado como []).

    Returns:
        Formatted string with context and numbered sources.

    Note (audit rodada 2 BUG-10/11):
        Mesmo após sanitização, `topic` ainda pode conter `\r` (carriage
        return) que faz terminals apagarem a linha anterior — exibido
        via `f"## Research: {topic}"`. Aplicamos um segundo filtro para
        normalizar control chars residuais antes da interpolação.
    """
    # Audit rodada 3: defensivo contra callers que passam None em sources.
    if not sources:
        sources_list = []
    elif isinstance(sources, list):
        sources_list = sources
    else:
        # Tentamos iterar; se não for iterável, fallback para []
        try:
            sources_list = list(sources)
        except TypeError:
            sources_list = []

    # Segunda camada de defesa — neutraliza control chars que passaram.
    topic_str = topic if isinstance(topic, str) else str(topic or "")
    safe_topic = topic_str.replace("\r", " ").replace("\n", " ").replace("\t", " ")
    while "  " in safe_topic:
        safe_topic = safe_topic.replace("  ", " ")

    formatted = f"## Research: {safe_topic}\n\n{context}\n\n"
    formatted += "## Sources:\n"
    for i, source in enumerate(sources_list):
        formatted += f"{i + 1}. {source.get('title', 'Unknown')}: {source.get('url', '')}\n"
    return formatted


