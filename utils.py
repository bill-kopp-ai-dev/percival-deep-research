from collections import defaultdict
from dataclasses import dataclass, field
from threading import Lock


@dataclass
class Metrics:
    """Contadores básicos de operação (thread-safe)."""
    _lock: Lock = field(default_factory=Lock)
    deep_research_total: int = 0
    deep_research_errors: int = 0
    deep_research_timeout: int = 0
    quick_search_total: int = 0
    write_report_total: int = 0
    # Note (audit rodada 3): cache_hits/cache_misses foram removidos — eram
    # dead fields (ninguém os incrementava). Use `timeouts_by_tool` /
    # `errors_by_tool` para rastreamento real, ou rastreie hits/misses
    # externamente se necessário.
    # deque(maxlen=100) para latências: append O(1), evict automático O(1).
    _latencies_ms: deque = field(default_factory=lambda: deque(maxlen=100))
    _timeouts_by_tool: dict = field(default_factory=lambda: defaultdict(int))
    _errors_by_tool: dict = field(default_factory=lambda: defaultdict(int))

    def record_latency(self, operation: str, ms: float) -> None:
        with self._lock:
            key = f"{operation}_total"
            if hasattr(self, key):
                setattr(self, key, getattr(self, key) + 1)
            self._latencies_ms.append(ms)
            # deque(maxlen=100) tem O(1) amortizado (vs O(n) de list.pop(0)).
            # Audit rodada 2 BUG-Metrics: otimização hot path.

    def record_timeout(self, operation: str) -> None:
        with self._lock:
            self._timeouts_by_tool[operation] += 1
            if operation == "deep_research":
                self.deep_research_timeout += 1

    def record_error(self, operation: str) -> None:
        with self._lock:
            self._errors_by_tool[operation] += 1
            if operation == "deep_research":
                self.deep_research_errors += 1

    def snapshot(self) -> dict:
        with self._lock:
            # `sorted(deque)` funciona igual a list; só custo extra de
            # converter para list materializado é N log N.
            sorted_latencies = sorted(self._latencies_ms) if self._latencies_ms else []
            return {
                "deep_research_total": self.deep_research_total,
                "deep_research_errors": self.deep_research_errors,
                "deep_research_timeout": self.deep_research_timeout,
                "quick_search_total": self.quick_search_total,
                "write_report_total": self.write_report_total,
                "timeouts_by_tool": dict(self._timeouts_by_tool),
                "errors_by_tool": dict(self._errors_by_tool),
                "p50_latency_ms": (
                    sorted_latencies[len(sorted_latencies) // 2]
                    if sorted_latencies else 0
                ),
            }


def rate_limited(limiter: RateLimiter):
    """Decorator: limita concorrência via RateLimiter. O `decorator` é
    síncrono (apenas embrulha a função); só `wrapper` é async."""
    def decorator(func):
        @functools.wraps(func)
        async def wrapper(*args, **kwargs):
            await limiter.acquire()
            try:
                return await func(*args, **kwargs)
            finally:
                limiter.release()
        return wrapper
    return decorator


def handle_exception(
    e: Exception, operation: str, correlation_id: str | None = None
) -> str:
    """
    Handles exceptions without leaking internal details to the agent.

    The full stack trace is logged to stderr (visible to the operator only).
    The agent receives only a safe, generic plain-text error message starting
    with 'Error:' so that Nanobot's runner can detect it as a tool error and
    append its debugging hint automatically. The `correlation_id` allows the
    operator to find the matching log entry.
    """
    cid = correlation_id or new_correlation_id()
    # Full log internally — Fix [A1]
    logger.exception(f"{operation} failed (cid={cid})")
    # Generic, safe plain-text message for the agent
    return (
        f"Error: Failed to execute '{operation}' "
        f"(correlation_id={cid}). "
        f"Check the server logs for details."
    )


