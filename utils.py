class RegistryFullError(RuntimeError):
    """Levantada por `ResearchRegistry.add_researcher` quando a capacidade
    está saturada e a registry rejeita nova inscrição.

    Herda de `RuntimeError` para que callers que tratam erros genéricos
    também consigam capturar.
    """


class ResearchRegistry:
    """
    Centralized registry for active research sessions and cached results.

    Implements:
    - Maximum entry limit (prevents memory leak)
    - Automatic TTL-based eviction (prevents persistent cache poisoning)
    - FIFO eviction when the capacity limit is reached
    - Thread-safe operations
    """

    _MAX_RESEARCHERS = 50       # maximum simultaneous active researcher objects
    _RESEARCHER_TTL_S = 3_600   # 1-hour TTL for researcher objects
    _MAX_CACHED_TOPICS = 100    # maximum number of topics in the context cache
    _CACHE_TOPIC_TTL_S = 3_600  # 1h TTL for cached topics

    def __init__(self, settings=None) -> None:
        # Quando `settings` é fornecido, é usado. Sem settings, delega para
        # os defaults de classe (acessíveis via property abaixo).
        self._settings = settings
        # Maps research_id -> GPTResearcher object
        self._researchers: Dict[str, Any] = {}
        # Creation timestamps for researcher objects (used for TTL and FIFO eviction)
        self._researcher_ts: Dict[str, float] = {}
        # Maps topic/query -> formatted context with sources (cache)
        self._store: Dict[str, Dict[str, Any]] = {}
        # Creation timestamps for cached topics (TTL)
        self._store_ts: Dict[str, float] = {}
        # Lock para tornar todas as operações thread-safe
        self._lock = threading.RLock()

    @property
    def _max_researchers(self) -> int:
        """Max active researchers. Settings override o default de classe."""
        if self._settings is not None:
            return self._settings.max_researchers
        return type(self)._MAX_RESEARCHERS

    @property
    def _researcher_ttl_s(self) -> int:
        """TTL em segundos para researchers. Settings override."""
        if self._settings is not None:
            return self._settings.researcher_ttl_s
        return type(self)._RESEARCHER_TTL_S

    @property
    def _max_cached_topics(self) -> int:
        """Max topics no cache de contexto. Settings override."""
        if self._settings is not None:
            return self._settings.max_cached_topics
        return type(self)._MAX_CACHED_TOPICS

    @property
    def _cache_topic_ttl_s(self) -> int:
        """TTL em segundos para cache de tópicos. Settings override."""
        if self._settings is not None:
            return self._settings.cache_topic_ttl_s
        return type(self)._CACHE_TOPIC_TTL_S

    # ── Active researchers ────────────────────

    def add_researcher(self, research_id: str, researcher: Any) -> None:
        """Registers a GPTResearcher object with TTL and capacity enforcement.

        Audit rodada 2 BUG-5: antes fazia FIFO evict ao saturar, derrubando
        arbitrary sessions belonging to other agents. Agora rejeita com
        `RegistryFullError` para que o caller decida (ex.: retornar 503).
        """
        with self._lock:
            self._evict_expired()

            # Rejeita em vez de evict arbitrário — derrubar a sessão de
            # outro agente silenciosamente é pior do que recusar a nova.
            if len(self._researchers) >= self._max_researchers:
                raise RegistryFullError(
                    f"Researcher registry is full ({self._max_researchers}). "
                    f"Wait for an existing session to expire or complete."
                )

            self._researchers[research_id] = researcher
            self._researcher_ts[research_id] = time.monotonic()

    def get_researcher(self, research_id: str) -> Tuple[bool, Any, Dict[str, Any]]:
        """
        Retrieves a researcher by ID, checking TTL on access.

        Returns:
            Tuple of (success, researcher_object, error_response).
        """
        with self._lock:
            self._evict_expired()
            if research_id not in self._researchers:
                return False, None, create_error_response(
                    "Research ID not found or expired. "
                    "Please run a new research session."
                )
            return True, self._researchers[research_id], {}

    def evict_researcher(self, research_id: str) -> bool:
        """Evicção explícita de um researcher (ex.: em erro de extração).

        Útil para liberar recursos imediatamente (HTTP clients, contexto)
        em vez de esperar o TTL. Idempotente — retorna True se removeu,
        False se não estava presente.
        """
        with self._lock:
            if research_id in self._researchers:
                self._researchers.pop(research_id, None)
                self._researcher_ts.pop(research_id, None)
                logger.info(f"Evicted researcher explicitly: {research_id}")
                return True
            return False

    def _evict_expired(self) -> None:
        """Removes researcher objects that have exceeded their TTL."""
        now = time.monotonic()
        ttl = self._researcher_ttl_s  # property — chama uma vez
        expired = [
            rid for rid, ts in self._researcher_ts.items()
            if now - ts > ttl
        ]
        for rid in expired:
            logger.info(f"Evicting expired researcher: {rid}")
            self._researchers.pop(rid, None)
            self._researcher_ts.pop(rid, None)

    # ── Research cache ────────────────────────

    def has_topic(self, topic: str) -> bool:
        """Returns True if research for this topic is already cached."""
        with self._lock:
            self._evict_expired_cache()
            return topic in self._store

    def get_cached(self, topic: str) -> Optional[str]:
        """Returns the formatted cached context for a topic, or None."""
        with self._lock:
            self._evict_expired_cache()
            entry = self._store.get(topic)
            return entry["context"] if entry else None

    def store(
        self,
        topic: str,
        context: str,
        sources: List[Dict[str, Any]],
        source_urls: List[str],
        formatted_context: Optional[str] = None,
    ) -> None:
        """
        Stores research results in the internal cache.

        Enforces capacity limit with approximate FIFO eviction
        (removes the oldest entry when the limit is reached).
        Topic entries expire after `_CACHE_TOPIC_TTL_S`.
        """
        with self._lock:
            self._evict_expired_cache()

            # Usa a property (honra settings.max_cached_topics quando fornecido).
            if len(self._store) >= self._max_cached_topics:
                # Remove the oldest inserted item (approximate FIFO)
                oldest_topic = next(iter(self._store))
                logger.info(f"Cache full — evicting oldest topic: {oldest_topic!r}")
                self._store.pop(oldest_topic, None)
                self._store_ts.pop(oldest_topic, None)

            self._store[topic] = {
                "context": formatted_context or context,
                "sources": sources,
                "source_urls": source_urls,
            }
            self._store_ts[topic] = time.monotonic()

    def _evict_expired_cache(self) -> None:
        """Removes cached topics whose TTL foi excedido."""
        now = time.monotonic()
        ttl = self._cache_topic_ttl_s  # property — chama uma vez
        expired = [
            t for t, ts in self._store_ts.items()
            if now - ts > ttl
        ]
        for t in expired:
            logger.info(f"Cache expired for topic: {t!r}")
            self._store.pop(t, None)
            self._store_ts.pop(t, None)


